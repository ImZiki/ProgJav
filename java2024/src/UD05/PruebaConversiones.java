package UD05;

public class PruebaConversiones {
    public static void main(String[] args) {
        char letraA = 65;
        System.out.println(letraA);
        int valorA = letraA;
        System.out.println(valorA);
        int valorB = 'B';
        System.out.println(valorB);
        char letraB = (char)valorB;
        System.out.println(letraB);
    }
}
